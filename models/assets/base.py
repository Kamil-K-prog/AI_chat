"""
AssetManager — ЕДИНСТВЕННОЕ место, где происходит I/O по ассетам:
загрузка в облако провайдера, OCR документов, и запись результата
(cloud_refs / ocr_text) ОБРАТНО в УФС.

Зачем выделено отдельно:
  - Конвертер становится чистым (без сети) и тестируемым.
  - Мутация истории сосредоточена в одном понятном месте — исчезает
    путаница "почему изменения ассета не сохранились" (в Python это
    ссылка, а не копия: мутация asset in-place видна везде).

prepare() проходит по всей истории один раз ПЕРЕД конвертацией и
догружает всё, что нужно, проставляя ссылки в asset.cloud_refs[provider].
"""

from abc import ABC, abstractmethod
import utils.types as t


class AssetManager(ABC):
    """Базовый интерфейс подготовки ассетов под конкретного провайдера."""

    @abstractmethod
    def prepare(self, history: t.ChatData) -> t.ChatData:
        """
        Пройти по истории и подготовить все ассеты к отправке.

        Мутирует ассеты in-place: проставляет cloud_refs/ocr_text там, где
        требуется загрузка в облако или извлечение текста. Идемпотентна —
        уже подготовленные ассеты повторно не загружает.

        :param history: История чата в УФС.
        :return: Та же история с дозаполненными ассетами (для чейнинга).
        """
        ...

    @abstractmethod
    def prepare_asset(self, asset: t.Asset) -> t.Asset:
        """
        Подготовить один ассет: при необходимости загрузить/распознать и
        записать результат в asset.cloud_refs / asset.ocr_text.

        :param asset: Ассет УФС.
        :return: Тот же ассет с дозаполненными полями.
        """
        ...
